  CREATE OR REPLACE FORCE VIEW "IDSTEST"."DATAVIEW" ("CTDC_THONG_TIN_CO_SO_ID", "BM_BAO_CAO_CT_ID", "NGAY_SO_LIEU", "LOAI_BAO_CAO_TAI_CHINH", "GIA_TRI_NHAP", "ROW_NUM") AS 
  SELECT gr1.CTDC_THONG_TIN_CO_SO_ID ,  gr1.BM_BAO_CAO_CT_ID , gr1.NGAY_SO_LIEU , gr1.LOAI_BAO_CAO_TAI_CHINH , 
       gr1.GIA_TRI_NHAP, ROWNUM as ROW_NUM
FROM (
  SELECT bc.CTDC_THONG_TIN_CO_SO_ID , bcgt.BM_BAO_CAO_CT_ID , bc.NGAY_SO_LIEU , bc.LOAI_BAO_CAO_TAI_CHINH , 
         LISTAGG(bc.TRANG_THAI_KIEM_TOAN||'='||bcgt.GIA_TRI, ',') WITHIN GROUP (ORDER BY bc.TRANG_THAI_KIEM_TOAN) "GIA_TRI_NHAP" 
         --,DENSE_RANK() OVER (ORDER BY NGAY_SO_LIEU  DESC , bc.CTDC_THONG_TIN_CO_SO_ID) AS grp
  FROM  BC_BAO_CAO bc JOIN BC_BAO_CAO_GT bcgt ON  bc.ID = bcgt.BC_BAO_CAO_ID
  WHERE bc.NGAY_SO_LIEU is not null AND bc.TRANG_THAI =3 
  --tim kiếm theo chỉ tiêu AND bcgt.BM_BAO_CAO_CT_ID
  -- tim kiếm theo ctdc AND bc.CTDC_THONG_TIN_CO_SO_ID
  --tim kiem theo nsl bc.NGAY_SO_LIEU
  GROUP BY bc.CTDC_THONG_TIN_CO_SO_ID , bcgt.BM_BAO_CAO_CT_ID , bc.NGAY_SO_LIEU , bc.LOAI_BAO_CAO_TAI_CHINH
  ORDER BY bc.NGAY_SO_LIEU  DESC , bc.CTDC_THONG_TIN_CO_SO_ID) gr1
;
